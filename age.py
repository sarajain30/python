age = int(input("how old are you : "))


if age <= 1:
    print("you're a BABY")
elif age > 1 and age <= 3:
    print("you are a toddler")
elif age > 3 and age <= 6:
    print("you are a small child")
elif age > 6 and age <= 11:
    print("you are a big kid")
elif age > 11 and age <= 17:
    print("you are teenager")
elif age > 17 and age <= 39:
    print("you are a young adult")
elif age > 39 and age <= 69:
    print("you are a middle aged adult")
elif age > 69 and age <= 89:
    print("you're old")
elif age > 89 and age <= 110:
    print("you are very old")
else:
    print("you are WAY TO OLD TO LIVE")













